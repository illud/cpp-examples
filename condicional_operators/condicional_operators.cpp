#include <iostream>

using namespace std;

int main(){
    // Condicional operators
    // == Equal to
    // != Not equal to
    // > Greater than
    // < Less than
    // >= Greater than or equal to
    // <= Less than or equal to

    // Declare variables
    int x = 8;
    int b = 4;

    // Print if x is equal to b
    cout << (x == b) << "\n";

    // Print if x is not equal to b
    cout << (x != b) << "\n";

    // Print if x is greater than b
    cout << (x > b) << "\n";

    // Print if x is less than b
    cout << (x < b) << "\n";

    // Print if x is greater than or equal to b
    cout << (x >= b) << "\n";

    // Print if x is less than or equal to b
    cout << (x <= b) << "\n";

    // Print if x is equal to b
    cout << (x == b) << "\n";
}