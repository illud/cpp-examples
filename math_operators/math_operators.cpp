#include <iostream>

using namespace std;

int main(){
    // Math operators
    // + Addition
    // - Subtraction
    // * Multiplication
    // / Division
    // % Modulo

    // Declare variables
    int x = 8;
    int b = 4;

    // Print the sum of x and b
    cout << x + b << "\n";

    // Print subtraction of x and b
    cout << x - b << "\n";

    // Print multiplication of x and b
    cout << x * b << "\n";

    // Print division of x and b
    cout << x / b << "\n";

    // Print modulo of x and b
    cout << x % b << "\n";
}